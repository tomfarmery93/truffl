  for update using (auth.uid() = auth_id);

create policy "Booking parties can view walk sessions" on public.walk_sessions
  for select using (booking_id in (select bookings.id from public.bookings
    where bookings.customer_id = (select customer_profiles.id from public.customer_profiles where customer_profiles.user_id = (select users.id from public.users where users.auth_id = auth.uid()))
       or bookings.provider_id = (select provider_profiles.id from public.provider_profiles where provider_profiles.user_id = (select users.id from public.users where users.auth_id = auth.uid()))));
create policy "Customers can view walk sessions for their bookings" on public.walk_sessions
  for select using (booking_id in (select bookings.id from public.bookings where bookings.customer_id in (select customer_profiles.id from public.customer_profiles where customer_profiles.user_id = auth.uid())));
create policy "Providers can view own walk sessions" on public.walk_sessions
  for select using (provider_id = auth.uid());
create policy "Providers can manage own walk sessions" on public.walk_sessions
  for all using (provider_id = (select provider_profiles.id from public.provider_profiles where provider_profiles.user_id = (select users.id from public.users where users.auth_id = auth.uid())));
create policy "Providers can create walk sessions" on public.walk_sessions
  for insert with check (provider_id = auth.uid());
create policy "Providers can update own walk sessions" on public.walk_sessions
  for update using (provider_id = auth.uid());

create policy "Customers can view updates for their bookings" on public.walk_updates
  for select using (walk_session_id in (select walk_sessions.id from public.walk_sessions where walk_sessions.booking_id in (select bookings.id from public.bookings where bookings.customer_id in (select customer_profiles.id from public.customer_profiles where customer_profiles.user_id = auth.uid()))));
create policy "Providers can view own walk updates" on public.walk_updates
  for select using (provider_id = auth.uid());
create policy "Providers can create walk updates" on public.walk_updates
  for insert with check (provider_id = auth.uid());

