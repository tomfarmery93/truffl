-- TRU-154 (stage 1): proper location search + same-day-nearby ranking.
--
-- Search previously fetched every carer and substring-matched the carer's HOME
-- suburb ("Homebush" never matched a Newington carer 3km away). This replaces it
-- with PostGIS matching against each carer's own service radius, using the
-- location data that already existed (provider_profiles.location + radius_km).
--
-- Staged design (decided with Tom 2026-07-03):
--   Stage 1 (this): crow-flies radius + covered-suburbs preview + same-day boost.
--   Stage 2 (ticketed): travel-time isochrones — computed ONCE per carer settings
--     save via a routing API, stored as a polygon; search_carers checks the
--     polygon first and falls back to radius, so it drops in with no UI rework.
--   Stage 3 (parked): per-carer suburb exclude list as an override.
--
-- Also consolidates the radius columns: register/ wrote service_radius_km while
-- everything reads radius_km, so a new carer's signup radius was ignored.
--
-- Applied to the live DB via apply_migration (+ seed via execute_sql, same
-- content); committed for version control.

-- ── Suburbs reference table (Greater Sydney + Central Coast/Wollongong/Blue
-- Mountains; locality centroids from the public australian_postcodes dataset).
-- Powers offline geocoding, search autocomplete and the covered-suburbs preview.
create table if not exists public.suburbs (
  id       serial primary key,
  name     text not null,
  postcode text not null,
  centroid geography(Point,4326) not null,
  unique (name, postcode)
);
create index if not exists idx_suburbs_name on public.suburbs (lower(name));
create index if not exists idx_suburbs_centroid on public.suburbs using gist (centroid);
alter table public.suburbs enable row level security;
drop policy if exists suburbs_public_read on public.suburbs;
create policy suburbs_public_read on public.suburbs for select using (true);
revoke insert, update, delete on public.suburbs from anon, authenticated;

-- Customers need coordinates too (suburb-centroid precision is plenty) for the
-- same-day-nearby ranking signal and, later, TRU-110's walker-side distances.
alter table public.customer_profiles
  add column if not exists location geography(Point,4326);

-- ── Offline geocoding ─────────────────────────────────────────────────────────
create or replace function public.suburb_centroid(p_suburb text, p_postcode text default null)
returns geography language sql stable as $$
  select s.centroid from public.suburbs s
  where lower(s.name) = lower(trim(coalesce(p_suburb, '')))
  order by (s.postcode = coalesce(trim(p_postcode), '')) desc, s.postcode
  limit 1;
$$;

-- Keep profile coordinates in step with the suburb/postcode the user saves —
-- covers every write path (register, profile, admin) with no client changes.
create or replace function public.trg_geocode_profile()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  if NEW.suburb is distinct from OLD.suburb
     or NEW.postcode is distinct from OLD.postcode
     or NEW.location is null then
    NEW.location := coalesce(public.suburb_centroid(NEW.suburb, NEW.postcode), NEW.location);
  end if;
  return NEW;
end;
$$;
revoke execute on function public.trg_geocode_profile() from public, anon, authenticated;

drop trigger if exists geocode_provider_profile on public.provider_profiles;
create trigger geocode_provider_profile
  before insert or update of suburb, postcode on public.provider_profiles
  for each row execute function public.trg_geocode_profile();
drop trigger if exists geocode_customer_profile on public.customer_profiles;
create trigger geocode_customer_profile
  before insert or update of suburb, postcode on public.customer_profiles
  for each row execute function public.trg_geocode_profile();

-- Radius consolidation: registration wrote service_radius_km; radius_km is what
-- search reads. Copy over, then the register page writes radius_km directly.
update public.provider_profiles
   set radius_km = service_radius_km
 where service_radius_km is not null and service_radius_km > 0;

-- ── Search (definer: ranks on RLS-protected booking data without exposing it) ──
-- Matching: carers whose own service radius covers the searched suburb.
-- Ranking data: distance, plus nearby_same_day — the carer already has a
-- confirmed walk within 3km of the searched suburb on the requested date, so
-- booking them builds a dense back-to-back route (the walker-efficiency signal).
-- Only a boolean leaves the DB; no addresses, times or counts.
create or replace function public.search_carers(
  p_suburb text default null,
  p_postcode text default null,
  p_date date default null
) returns table (
  id uuid, user_id uuid, first_name text, last_name text, avatar_url text,
  bio text, suburb text, radius_km integer, is_verified boolean, is_active boolean,
  avg_rating numeric, total_reviews integer, services json, created_at timestamptz,
  distance_km numeric, nearby_same_day boolean
)
language sql stable security definer set search_path = public as $$
  with pt as (
    select public.suburb_centroid(p_suburb, p_postcode) as g
  )
  select pp.id, pp.user_id, u.first_name, u.last_name, u.avatar_url,
    pp.bio, pp.suburb, pp.radius_km, pp.is_verified, pp.is_active,
    pp.avg_rating, pp.total_reviews,
    coalesce(json_agg(ps.service_type order by ps.service_type)
             filter (where ps.service_type is not null), '[]'::json) as services,
    pp.created_at,
    case when (select g from pt) is not null and pp.location is not null
         then round((st_distance(pp.location, (select g from pt)) / 1000.0)::numeric, 1)
    end as distance_km,
    case when p_date is not null and (select g from pt) is not null then exists (
      select 1
      from public.bookings b
      join public.customer_profiles cp on cp.id = b.customer_id
      where b.provider_id = pp.id
        and b.status in ('confirmed', 'in_progress')
        and not coalesce(b.is_meet_and_greet, false)
        and (b.scheduled_at at time zone 'Australia/Sydney')::date = p_date
        and cp.location is not null
        and st_dwithin(cp.location, (select g from pt), 3000)
    ) else false end as nearby_same_day
  from public.provider_profiles pp
  join public.users u on u.id = pp.user_id
  left join public.provider_services ps on ps.provider_id = pp.user_id and ps.is_available = true
  where pp.is_active = true
    and (
      nullif(trim(coalesce(p_suburb, '')), '') is null
      or ((select g from pt) is not null and pp.location is not null
          and st_dwithin(pp.location, (select g from pt), greatest(coalesce(pp.radius_km, 5), 1) * 1000))
      or (((select g from pt) is null or pp.location is null)
          and pp.suburb ilike '%' || p_suburb || '%')
    )
  group by pp.id, pp.user_id, u.first_name, u.last_name, u.avatar_url, pp.bio,
           pp.suburb, pp.radius_km, pp.is_verified, pp.is_active, pp.avg_rating,
           pp.total_reviews, pp.created_at, pp.location
  order by nearby_same_day desc, distance_km asc nulls last, pp.avg_rating desc nulls last
$$;
revoke all on function public.search_carers(text, text, date) from public;
grant execute on function public.search_carers(text, text, date) to anon, authenticated;

-- Covered-suburbs preview for the carer's service-area setting ("15 km covers:
-- Newington, Strathfield, ..."), so a crow-flies radius is legible on land
-- without a map dependency.
create or replace function public.my_covered_suburbs(p_radius_km integer)
returns table (name text, distance_km numeric)
language sql stable security definer set search_path = public as $$
  select s.name,
         round((st_distance(s.centroid, pp.location) / 1000.0)::numeric, 1) as distance_km
  from public.provider_profiles pp
  join public.suburbs s on st_dwithin(s.centroid, pp.location, greatest(coalesce(p_radius_km, 5), 1) * 1000)
  where pp.user_id = auth.uid() and pp.location is not null
  group by s.name, s.centroid, pp.location
  order by distance_km
  limit 80;
$$;
revoke all on function public.my_covered_suburbs(integer) from public, anon;
grant execute on function public.my_covered_suburbs(integer) to authenticated;

