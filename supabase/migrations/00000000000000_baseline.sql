-- ── Views (pre-existing only; definer-style, scoped by auth.uid() in WHERE) ────
create view public.active_walk_view as
  select ws.id as walk_session_id,
    ws.booking_id,
    ws.status,
    ws.started_at,
    ws.distance_metres,
    (select row_to_json(latest.*) from (select gps_pings.lat, gps_pings.lng, gps_pings.recorded_at
        from public.gps_pings where gps_pings.walk_session_id = ws.id
        order by gps_pings.recorded_at desc limit 1) latest) as latest_position,
    (select row_to_json(latest_update.*) from (select booking_updates.message, booking_updates.photo_url, booking_updates.sent_at
        from public.booking_updates where booking_updates.booking_id = ws.booking_id
        order by booking_updates.sent_at desc limit 1) latest_update) as latest_update
  from public.walk_sessions ws
  where ws.status = 'active'::text;

create view public.booking_party_names as
  select b.id as booking_id,
    cu.id as customer_user_id, cu.first_name as customer_first_name, cu.last_name as customer_last_name,
    pu.id as provider_user_id, pu.first_name as provider_first_name, pu.last_name as provider_last_name
  from public.bookings b
    join public.customer_profiles cp on cp.id = b.customer_id
    join public.users cu on cu.id = cp.user_id
    join public.provider_profiles pp on pp.id = b.provider_id
    join public.users pu on pu.id = pp.user_id
  where cp.user_id = auth.uid() or pp.user_id = auth.uid();

create view public.provider_rating_view as
  select provider_id, count(*) as review_count, round(avg(rating), 1) as avg_rating
  from public.reviews group by provider_id;

create view public.provider_search_view as
  select pp.id, pp.user_id, u.first_name, u.last_name, u.avatar_url, pp.bio, pp.suburb, pp.radius_km,
    pp.is_verified, pp.is_active, pp.avg_rating, pp.total_reviews,
    coalesce(json_agg(ps.service_type order by ps.service_type) filter (where ps.service_type is not null), '[]'::json) as services
  from public.provider_profiles pp
    join public.users u on u.id = pp.user_id
    left join public.provider_services ps on ps.provider_id = pp.user_id and ps.is_available = true
  where pp.is_active = true
  group by pp.id, pp.user_id, u.first_name, u.last_name, u.avatar_url, pp.bio, pp.suburb, pp.radius_km, pp.is_verified, pp.is_active, pp.avg_rating, pp.total_reviews;

create view public.series_party_names as
  select bs.id as series_id,
    cu.first_name as customer_first_name, cu.last_name as customer_last_name,
    pu.first_name as provider_first_name, pu.last_name as provider_last_name,
    (select count(*) from public.series_pets sp where sp.series_id = bs.id) as pet_count,
    (select string_agg(p.name, ', '::text order by p.name) from public.series_pets sp join public.pets p on p.id = sp.pet_id where sp.series_id = bs.id) as pet_names
  from public.booking_series bs
    join public.customer_profiles cp on cp.id = bs.customer_id
    join public.users cu on cu.id = cp.user_id
    join public.provider_profiles pp on pp.id = bs.provider_id
    join public.users pu on pu.id = pp.user_id
  where cp.user_id = auth.uid() or pp.user_id = auth.uid();

