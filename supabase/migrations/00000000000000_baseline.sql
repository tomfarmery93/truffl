-- TRU-145: baseline schema migration.
--
-- The core schema (users, bookings, booking_series, provider/customer profiles,
-- pets, walks, gps, etc.) was created directly in the live DB before the
-- supabase/migrations/ folder existed, so it was never captured as a migration.
-- The Supabase Preview (branch) check replays migrations from scratch on a fresh
-- DB and died on the first migration that referenced these tables.
--
-- This file reconstructs the schema AS IT WAS immediately BEFORE the first tracked
-- migration (20260612_recurring_walks_rolling_job.sql), so the incremental
-- migrations apply cleanly on top of it. It therefore intentionally OMITS anything
-- the later migrations create/alter, e.g.:
--   * tables conversations, series_decline_feedback, system_messages, private.*
--   * columns bookings.payment_status/window_end_at/…, booking_series.booking_kind,
--     messages.conversation_id, provider_profiles.stripe_*, users.is_admin, …
--   * pets.dob (added later) — baseline keeps the original pets.age_years, which
--     20260615_tru71_pet_dob.sql drops
--   * the series_status value 'pending_meet_greet' (added by 20260615_tru14)
--   * functions/views/policies/triggers that the migrations (re)create
--
-- Generated by reconstructing DDL from the live catalog (pg_get_*def, information_schema).

-- ── Extensions ──────────────────────────────────────────────────────────────
create extension if not exists "uuid-ossp" with schema extensions;
create extension if not exists postgis with schema public;

