-- ── Pre-existing functions ────────────────────────────────────────────────────
-- (Everything else is (re)created by a later migration via CREATE OR REPLACE.)
create or replace function public.set_updated_at()
returns trigger language plpgsql as $function$
begin
  new.updated_at = now();
  return new;
end;
$function$;

create or replace function public.handle_new_user()
returns trigger language plpgsql security definer as $function$
begin
  insert into public.users (id, auth_id, email, first_name, last_name, role)
  values (
    new.id,
    new.id,
    new.email,
    new.raw_user_meta_data->>'first_name',
    new.raw_user_meta_data->>'last_name',
    coalesce(new.raw_user_meta_data->>'role', 'customer')
  );
  return new;
end;
$function$;

-- Body references public.generate_series_bookings (created later by 20260613); the
-- reference is late-bound (plpgsql) so this is safe to define here.
create or replace function public.trg_series_activated()
returns trigger language plpgsql security definer set search_path to 'public' as $function$
begin
  if new.status = 'active' and (old.status is distinct from 'active') then
    perform public.generate_series_bookings(new.id);
  end if;
  return new;
end;
$function$;

